#include "libft.h"

char	*ft_strncpy(char *dst, char const *src, int len)
{
	int i;

	i = 0;
	while (i < len && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
